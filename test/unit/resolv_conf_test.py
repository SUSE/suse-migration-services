    def test_existing_static_live(self, setup_env):
        target_root, dest_dir = setup_env
        resolv_src = target_root / "etc/resolv.conf"
        resolv_src.write_text(CUSTOM_CONTENT)

        static_file = dest_dir / "etc/resolv.conf.static"
        static_file.write_text("old content")

        rc = ResolvConf()
        rc.setup_live_system(dest_dir)

        # resolv.conf (link) -> resolv.conf.static-TIMESTAMP
        link_target = os.readlink(str(dest_dir / "etc/resolv.conf"))
        assert link_target.startswith("resolv.conf.static-")
        assert (dest_dir / 'etc' / link_target).read_text() == CUSTOM_CONTENT
        assert static_file.read_text() == "old content"

    def test_invalid_link_live(self, setup_env):
        global LINK_NAME
        target_root, dest_dir = setup_env
        LINK_NAME = ''
        target_link = target_root / "etc/resolv.conf"
        os.symlink('I DO NOT EXISTS', target_link)

        rc = ResolvConf()
        rc.setup_live_system(dest_dir)
        rc.setup_target_root()

        assert not (dest_dir / 'etc/resolv.conf').is_symlink()
        assert (target_root / 'etc/resolv.conf').is_symlink()

