    def test_netconfig_mismatch(self, setup_env):
        target_root, dest_dir = setup_env
        resolv_file = target_root / "etc/resolv.conf"
        resolv_file.write_text(NETCONFIG_CONTENT.replace("opensuse.org", "suse.com"))

